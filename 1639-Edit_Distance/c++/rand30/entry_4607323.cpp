int gcd(int a,int b){//輾轉相除法
    while(b){
        int tmp=b;
        b=a%b;
        a=tmp;
    }
    return a;
}
ll qpow(ll base,ll exp){//快速冪
    ll rec=1;
    while(exp){
        if(exp&1)rec*=base;
        base*=base;
        exp>>=1;
    }
    return rec;
}
